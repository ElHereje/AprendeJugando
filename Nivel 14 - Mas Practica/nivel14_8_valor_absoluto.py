'''
la función abs devuelve el valor absoluto de un número
'''

print(abs(-3))

''' 
un ejemplo de uso puede ser calcular la distancia de 2 naves:
'''
x1 = 2
x2 = 7
distancia = abs(x1 - x2)
print(distancia)  # --> 5

x1 = -3
x2 = 4
distancia = abs(x1 - x2)
print(distancia)  # --> 7

x1 = -5
x2 = -2
distancia = abs(x1 - x2)
print(distancia)  # --> 3

#  Otro ejemplo podría ser para calcular un cambio de temperatura:
t1 = 10
t2 = -5
cambio = abs(t1 - t2)
print(cambio)  # --> 15



